void stampaLabirinto(int m[LAB_ALT][LAB_LUN])
{
	for (int i = 0; i < LAB_ALT; i++)
	{
		for (int i2 = 0; i2 < LAB_LUN; i2++)
		{
			char c;
			switch (m[i][i2])
			{
			case CELLA_MURO:
				c = '#';
				break;
			case CELLA_CALPESTATO:
				c = '.';
				break;
			case CELLA_ARRIVO:
				c = 'X';
				break;
			case CELLA_LIBERO:
			default:
				c = ' ';
				break;
			}
			std::cout << c << c;
		}
		std::cout << std::endl;
	}
}



